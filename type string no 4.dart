void main() {
  int a = 5;
  int b = 10;

  // Perkalian
  int perkalian = a * b;
  print("Perkalian: $perkalian");

  // Pembagian
  double pembagian = b / a;
  print("Pembagian: $pembagian");

  // Penjumlahan
  int penjumlahan = a + b;
  print("Penjumlahan: $penjumlahan");

  // Pengurangan
  int pengurangan = a - b;
  print("Pengurangan: $pengurangan");
}
